-- 함수 (숫자)

-- ABS : 절대값
SELECT ABS(-123) ,-123
FROM DUAL;


-- ROUND : 반올림
SELECT ROUND(3.14, 1)
FROM DUAL;

-- TRUNC :
SELECT TRUNC(10/3, 3), 10/3
FROM DUAL;

-- MOD : 나머지
SELECT MOD(10,3)
FROM DUAL
;

--POWER : 제곱
SELECT POWER(2,3)
FROM DUAL;

--SQRT : 제곱근
SELECT SQRT(25)
FROM DUAL;

